type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (value: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const stored = window.localStorage.getItem(LOCALE_KEY) as Locale | null;
    return stored === 'en-US' ? 'en-US' : 'zh-CN';
  });

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale: (next) => {
      setLocaleState(next);
      window.localStorage.setItem(LOCALE_KEY, next);
    },
    t: (input) => translateLoose(input, locale),
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}

export function translateLoose(input: string, locale: Locale): string {
  if (locale === 'en-US') return input;
  return ZH_MAP[input] ?? input;
}

export function formatCurrentPersonaTitle(personaName: string | null, locale: Locale): string {
  if (locale === 'en-US') return personaName ? `Current persona: ${personaName}` : 'Open chat';
  return personaName ? `当前人格：${personaName}` : '打开聊天';
}

export function formatCopyLabel(label: string, locale: Locale): string {
  if (locale === 'en-US') return label;
  return ZH_MAP[label] ?? label;
}
